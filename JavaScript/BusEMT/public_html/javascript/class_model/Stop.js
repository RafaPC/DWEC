class Stop {
    stopId
    name
    latitude
    longitude

    getStopId() {
        return stopId;
    }

    setStopId(stopId) {
        this.stopId = stopId;
    }

    getName() {
        return name;
    }

    setName(name) {
        this.name = name;
    }

    getLatitude() {
        return latitude;
    }

    setLatitude(latitude) {
        this.latitude = latitude;
    }

    getLongitude() {
        return longitude;
    }

    setLongitude(longitude) {
        this.longitude = longitude;
    }

}
